package tesla;

import processing.core.PApplet;

abstract class Carro {

	protected PApplet app;
	protected int batteryRange;
	protected int cargo;
	protected int display;
	protected float widthBar;
	protected float heightBar;
	
	
	
	
	
	public Carro(PApplet app) {
		this.app = app;
		
	}
	
	
	
	
	
}
